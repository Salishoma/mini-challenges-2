Object.freeze(MORSE_CODE);

function morse(text) {
    if(!text) return ""
    const deMorseArr = [];
    const codes = text.split(' ');
    let str = "";
    let isWord = true;
    for(let code of codes){
        if(code === ""){
            if(isWord){
                deMorseArr.push(str);
                str = "";
                isWord = false;
            }
        }
        else{
            isWord = true;
            str = `${str}${MORSE_CODE[code]}`;
        }
    }
    deMorseArr.push(str);
    return deMorseArr.join(' ').trim()
}

